import os
from groq import Groq
import json

class RAGEvaluator:
    def __init__(self):
        self.api_key = os.getenv("GROQ_API_KEY")
        self.client = Groq(api_key=self.api_key) if self.api_key else None

    def _fallback_evaluate(self, answer, context):
        if not context:
            return {"context_score": 0.0, "hallucination": True, "confidence": 0.0}
        overlap = sum(1 for word in answer.split() if word in context)
        context_score = overlap / max(len(answer.split()), 1)
        hallucination = len(answer) > len(context) * 1.5
        confidence = (0.3 * context_score) if hallucination else (0.7 + (0.3 * context_score))
        return {
            "context_score": round(context_score, 2),
            "hallucination": hallucination,
            "confidence": round(confidence, 2)
        }

    def evaluate(self, query, answer, context):
        if not self.client:
            return self._fallback_evaluate(answer, context)
            
        prompt = f"""
        You are an evaluator grading an answer generated by an AI assistant using a retrieved context.
        Query: {query}
        Context: {context}
        Answer: {answer}

        Evaluate the answer strictly according to whether it is faithful to the provided context and whether it introduces hallucinations (facts not present in the context).
        Return ONLY a JSON object with the following schema:
        {{
            "faithfulness_score": 0.0 to 1.0 (float representing how well the answer sticks to the context),
            "hallucination": true or false (boolean, true if there are hallucinations),
            "confidence": 0.0 to 1.0 (float representing overall score)
        }}
        """
        
        try:
            response = self.client.chat.completions.create(
                model="llama-3.1-8b-instant",
                messages=[{"role": "user", "content": prompt}],
                temperature=0,
                response_format={"type": "json_object"}
            )
            result = json.loads(response.choices[0].message.content.strip())
            return {
                "context_score": result.get("faithfulness_score", 0.0),
                "hallucination": result.get("hallucination", False),
                "confidence": result.get("confidence", 0.0)
            }
        except Exception:
            return self._fallback_evaluate(answer, context)